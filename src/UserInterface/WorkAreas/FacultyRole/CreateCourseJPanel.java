    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
       
        Double price = Double.parseDouble(txtprice.getText());
        String coursename = txtcoursename.getText();
        int credit = Integer.parseInt(txtcredit.getText());
        String code = txtcode.getText();
        
        
        Course cl = new Course();

        
        cl.setCode(code);
        cl.setCoursename(coursename);
        cl.setCredit(credit);
        cl.setPrice(price);
        courselist.addNewCourse(cl);
        
        ArrayList<Course> list =  this.term.getProfessorCourseMap().getOrDefault(this.current_user, new ArrayList<>());
        list.add(cl);
        this.term.getProfessorCourseMap().put(this.current_user,list);
        JOptionPane.showMessageDialog(this, "Course Updated");
        
        txtcode.setText("");
        txtcoursename.setText("");
        txtcredit.setText("");
        txtprice.setText("");
        populateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnselectfortermActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselectfortermActionPerformed
        // TODO add your handling code here:
      int selectedRow = tblcourse.getSelectedRow();
      
    
    if (selectedRow == -1) {
        
        JOptionPane.showMessageDialog(this, "Please select a course to add to the term.");
    } else {
        Course newcourse = new Course();
        DefaultTableModel model = (DefaultTableModel) tblcourse.getModel();
        newcourse.setCoursename((String)model.getValueAt(selectedRow,0));
        newcourse.setCode((String)model.getValueAt(selectedRow,1));
        newcourse.setPrice((Double)model.getValueAt(selectedRow,2));
        newcourse.setCredit((Integer)model.getValueAt(selectedRow,3));
      
       
        
        
        term.addNewCourseOffering(newcourse, current_user);
       
        
        JOptionPane.showMessageDialog(this, "Course added to the term.");
    }
    }//GEN-LAST:event_btnselectfortermActionPerformed

