    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblcourse.getSelectedRow();
        if (selectedRow == -1) {
        
        JOptionPane.showMessageDialog(this, "Please select a course to add to the term.");
    }
        else{
            ArrayList<Course> courseList = this.term.getProfessorCourseMap().getOrDefault(this.current_user, new ArrayList<>());
            DefaultTableModel model = (DefaultTableModel) tblcourse.getModel();
            String courseCode = (String)model.getValueAt(selectedRow, 1);
            for(int i=0;i<courseList.size();i++){
                if (courseList.get(i).getCode().equals(courseCode)){
                    courseList.remove(i);
                    break;
                }
            }
        populateTable();
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


