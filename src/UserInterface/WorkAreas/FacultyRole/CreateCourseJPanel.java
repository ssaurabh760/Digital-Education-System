    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblcourse.getModel();
        model.setRowCount(0);
        ArrayList<Course> list = this.term.getProfessorCourseMap().get(this.current_user);
        for (Course c : list){
        
            Object[] row = new Object[4];
            row[0] = c.getCoursename();
            row[1] = c.getCode();
            row[2] = c.getPrice();
            row[3] = c.getCredit();
            
            
            model.addRow(row);
        
        
        
        }   
        
}
