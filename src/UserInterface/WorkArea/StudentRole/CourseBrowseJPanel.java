    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        model.setRowCount(0);
        for (CourseOffering co : term.getColist()){
            Object[] row = new Object[6];
            row[0] = co;
            row[1] = co.getRanking();
            row[2] = co.getProfessor().getName();
            row[3] = co.getTerm().getName();
            row[4] = co.getSchedule();
            row[5] = co.getProfessor().getSpecialty();

            model.addRow(row);
        }
    }
