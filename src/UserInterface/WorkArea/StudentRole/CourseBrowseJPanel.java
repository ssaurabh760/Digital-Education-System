    private void btnViewCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewCourseActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblCourses.getSelectedRow();
//        
        if (selectedRowIndex<0) { 
            JOptionPane.showMessageDialog(this, "Please select a row to be displayed.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        CourseOffering co = (CourseOffering) model.getValueAt(selectedRowIndex, 0);
        CourseDetailForm detailPanel = new CourseDetailForm(term, userManager, current_user, co);
        MainFrame.mfInstance.setMainframeSplitFrame(detailPanel);
    }//GEN-LAST:event_btnViewCourseActionPerformed

